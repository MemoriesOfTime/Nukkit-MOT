    public static final int FLAG_SERVER = 0b1;
    public static final int FLAG_MUTE_ANNOUNCEMENT = 0b10;

    public long runtimeId;
    /**
     * @since v588
     */
    public String xuid;
    /**
     * @since 588
     */
    public String platformId;
    public String emoteID;
    public byte flags;
    /**
     * @since v729
     */
